    /**
     * Maneja la acción de registrar un nuevo usuario en el sistema a partir
     * de los datos introducidos en la interfaz. Si el registro es exitoso,
     * actualiza los combos y listas relacionadas.
     *
     * @param evt evento generado por la acción del usuario
     */
    private void AgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarUsuarioActionPerformed
        String nombre = NombreUsuarioManual.getText().trim();
        String tipoSeleccionado = TipoUsuarioManual.getSelectedItem().toString();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debes ingresar el nombre del usuario.",
                    "Campo vacío",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        String tipo;
        switch (tipoSeleccionado) {
            case "Prioridad Alta":
                tipo = "prioridad_alta";
                break;
            case "Prioridad Media":
                tipo = "prioridad_media";
                break;
            default:
                tipo = "regular";
                break;
        }
        boolean agregado = impresora.agregarUsuario(nombre, tipo);

        if (agregado) {
            JOptionPane.showMessageDialog(this, "Usuario agregado correctamente.");
            NombreUsuarioManual.setText("");
            mostrarUsuariosRegistrados();
            actualizarComboEliminarUsuarios();
            actualizarCombosUsuariosDocumentos();
            actualizarComboUsuariosEnCola();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Ese usuario ya existe.",
                    "Usuario duplicado",
                    JOptionPane.ERROR_MESSAGE);
        }    
        
    }//GEN-LAST:event_AgregarUsuarioActionPerformed

