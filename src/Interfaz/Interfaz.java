    /**
    * Construye el texto que representa la secuencia actual de impresión,
    * mostrando de forma ordenada los documentos presentes en la cola.
    *
    * @return representación textual de la secuencia de impresión
    */
    private String construirTextoSecuencia() {
        MonticuloBinario cola = impresora.getColaImpresion();

        if (cola == null || cola.esVacio()) {
            return "La cola de impresión está vacía.";
        }
        StringBuilder sb = new StringBuilder();
        RegistroImpresion[] arreglo = cola.getArregloInterno();
        int tam = cola.getTamano();

        for (int i = 0; i < tam; i++) {
            RegistroImpresion reg = arreglo[i];
            if (reg != null) {
                Documento doc = reg.getDocumento();

                sb.append("Posición ").append(i)
                  .append(" | Documento: ").append(doc.getId())
                  .append(" | Tipo: ").append(doc.getTipo())
                  .append(" | Tag: ").append(reg.getTimetag())
                  .append("\n");
            }
        }
        return sb.toString();
    }
    
    /**
    * Construye la representación en forma de árbol del montículo binario
    * utilizado para modelar la cola de impresión, con fines de visualización
    * dentro de la interfaz.
    *
    * @return nodo raíz del árbol visual que representa el heap
    */
    private DefaultMutableTreeNode construirArbolHeap() {
        MonticuloBinario cola = impresora.getColaImpresion();

        if (cola == null || cola.esVacio()) {
            return new DefaultMutableTreeNode("Cola vacía");
        }

        RegistroImpresion[] arreglo = cola.getArregloInterno();
        int tam = cola.getTamano();

        DefaultMutableTreeNode[] nodos = new DefaultMutableTreeNode[tam];

        for (int i = 0; i < tam; i++) {
            RegistroImpresion reg = arreglo[i];
            Documento doc = reg.getDocumento();

            String textoNodo = doc.getId() + " (tag=" + reg.getTimetag() + ")";
            nodos[i] = new DefaultMutableTreeNode(textoNodo);
        }

        for (int i = 0; i < tam; i++) {
            int izq = 2 * i + 1;
            int der = 2 * i + 2;

            if (izq < tam) {
                nodos[i].add(nodos[izq]);
            }
            if (der < tam) {
                nodos[i].add(nodos[der]);
            }
        }
        return nodos[0];
}
    
    /**
    * Actualiza el combo de usuarios que actualmente tienen documentos en cola,
    * a partir del contenido real de la cola de impresión.
    */
    private void actualizarComboUsuariosEnCola() {
        UsuarioCola.removeAllItems();

        ListaDocs<String> usuariosEnCola = impresora.getUsuariosConDocumentosEnCola();

        if (usuariosEnCola == null || usuariosEnCola.esVacia()) {
            UsuarioCola.setEnabled(false);
            DocumentoCola.removeAllItems();
            DocumentoCola.setEnabled(false);
            EliminarEnCola.setEnabled(false);
            return;
        }

        NodoLista<String> actualUsuario = usuariosEnCola.getFirst();
        while (actualUsuario != null) {
            UsuarioCola.addItem(actualUsuario.getInfo());
            actualUsuario = actualUsuario.getNext();
        }

        UsuarioCola.setEnabled(true);
        actualizarComboDocumentosEnCola();
    }
    
