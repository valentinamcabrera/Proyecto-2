    /**
 * Maneja la acción de enviar un documento a la cola de impresión.
 * Toma el usuario y el documento seleccionados, junto con la opción
 * de prioridad, y solicita al backend su inserción en la cola.Si el usuario
 * es de prioridad alta y no esta marcado como prioritario, el sistema pregunta 
 * si desea marcarlo.
 *
 * @param evt evento generado por la acción del usuario
 */
    private void EnviarImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarImprimirActionPerformed
        if (UsuarioGestionDoc.getSelectedItem() == null || DocumentoGestion.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this,
                    "Debes seleccionar un usuario y un documento.",
                    "Selección incompleta",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String nombreUsuario = UsuarioGestionDoc.getSelectedItem().toString();
        String nombreDocumento = DocumentoGestion.getSelectedItem().toString();
        boolean prioritario = chkPrioritario.isSelected();
        Usuario usuario = impresora.buscarUsuario(nombreUsuario);

        if (usuario != null && !prioritario) {
            String tipo = usuario.getTipo().toLowerCase();

            if (tipo.equals("prioridad_alta") || tipo.equals("prioridad_media")) {
                int opcion = JOptionPane.showConfirmDialog(
                        this,
                        "Este usuario es de " + tipo.replace("_", " ") +
                        ". ¿Desea marcar el documento como prioritario?",
                        "Confirmar prioridad",
                        JOptionPane.YES_NO_CANCEL_OPTION,
                        JOptionPane.QUESTION_MESSAGE
                );

                if (opcion == JOptionPane.CANCEL_OPTION) {
                    return;
                }

                if (opcion == JOptionPane.YES_OPTION) {
                    chkPrioritario.setSelected(true);
                    prioritario = true;
                }
            }
        }
        boolean enviado = impresora.enviarImprimir(nombreUsuario, nombreDocumento, prioritario);
        if (enviado) {
            JOptionPane.showMessageDialog(this,
                    "Documento enviado a imprimir correctamente.");
            actualizarComboDocumentosGestion();
            mostrarUsuariosRegistrados();
            chkPrioritario.setSelected(false);
            refrescarVistaCola();
            actualizarComboUsuariosEnCola();
        } else {
            JOptionPane.showMessageDialog(this,
                    "No se pudo enviar el documento a imprimir.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }     
    }//GEN-LAST:event_EnviarImprimirActionPerformed
   
