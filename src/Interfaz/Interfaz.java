    /**
    * Maneja la acción de cargar usuarios desde un archivo CSV seleccionado
    * mediante un selector de archivos. Si la carga es exitosa, actualiza
    * los componentes visuales relacionados con usuarios y cola.
    *
    * @param evt evento generado por la acción del usuario
    */
    private void CargarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarUsuariosActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Selecciona el archivo CSV de usuarios");
        int resultado = chooser.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = chooser.getSelectedFile();

            try {
                impresora.inicializarUsuariosDesdeCSV(archivo);
                JOptionPane.showMessageDialog(this, "Usuarios cargados correctamente.");
                mostrarUsuariosRegistrados();
                actualizarComboEliminarUsuarios();
                actualizarCombosUsuariosDocumentos();
                actualizarComboUsuariosEnCola();
            } catch (HeadlessException | IOException ex) {
                JOptionPane.showMessageDialog(this,
                        "Error al cargar el archivo: " + ex.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);}
        }
    }//GEN-LAST:event_CargarUsuariosActionPerformed
   
