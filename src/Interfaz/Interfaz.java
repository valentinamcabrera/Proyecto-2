    /**
 * Maneja la acción de crear un nuevo documento asociado al usuario
 * seleccionado en la interfaz, utilizando los datos de nombre y
 * número de páginas introducidos por el usuario.
 *
 * @param evt evento generado por la acción del usuario
 */
    private void CrearDocumentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearDocumentoActionPerformed
        String nombreUsuario = UsuarioCrearDoc.getSelectedItem() != null
                ? UsuarioCrearDoc.getSelectedItem().toString()
                : "";

        String nombreDocumento = NombreDocCrear.getText().trim();
        String paginasTexto = TamanoDocCrear.getText().trim();
        String tipoDocumento = TipoDocCrear.getSelectedItem() != null
                ? TipoDocCrear.getSelectedItem().toString()
                : "";

        if (nombreUsuario.isEmpty() || nombreDocumento.isEmpty() || paginasTexto.isEmpty() || tipoDocumento.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debes completar todos los campos para crear el documento.",
                    "Campos incompletos",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int paginas;
        try {
            paginas = Integer.parseInt(paginasTexto);
            if (paginas <= 0) {
                JOptionPane.showMessageDialog(this,
                        "El número de páginas debe ser mayor que cero.",
                        "Dato inválido",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "El número de páginas debe ser un valor numérico.",
                    "Error de formato",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean creado = impresora.crearDocumento(nombreUsuario, nombreDocumento, paginas, tipoDocumento);

        if (creado) {
            JOptionPane.showMessageDialog(this,
                    "Documento creado correctamente.");
            NombreDocCrear.setText("");
            TamanoDocCrear.setText("");
            actualizarComboDocumentosGestion();
            mostrarUsuariosRegistrados();
        } else {
            JOptionPane.showMessageDialog(this,
                    "No se pudo crear el documento. Puede que el usuario no exista o que ya tenga un documento con ese nombre.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CrearDocumentoActionPerformed
   
