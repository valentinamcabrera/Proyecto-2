    /**
 * Actualiza el combo de usuarios disponibles para ser eliminados del sistema,
 * según la lista actual de usuarios registrados.
 */
    private void actualizarComboEliminarUsuarios() {
        UsuarioAEliminar.removeAllItems();

        ListaDocs<Usuario> lista = impresora.getUsuariosRegistrados();

        if (lista == null || lista.esVacia()) {
            UsuarioAEliminar.setEnabled(false);
            DocumentoCola.setEnabled(false);
            return;
        }

        NodoLista<Usuario> actual = lista.getFirst();
        while (actual != null) {
            UsuarioAEliminar.addItem(actual.getInfo().getNombre());
            actual = actual.getNext();
        }

        UsuarioAEliminar.setEnabled(true);
        DocumentoCola.setEnabled(true);

    }  
    
    /**
    * Muestra en la interfaz la lista de usuarios actualmente registrados
    * en el sistema.
    */
    private void mostrarUsuariosRegistrados() {
    ListaDocs<Usuario> listaUsuarios = impresora.getUsuariosRegistrados();

    if (listaUsuarios == null || listaUsuarios.esVacia()) {
        MostrarUsuarios.setText("No hay usuarios registrados.");
        return;
    }
    StringBuilder sb = new StringBuilder();
    NodoLista<Usuario> actualUsuario = listaUsuarios.getFirst();
    int i = 1;
    while (actualUsuario != null) {
        Usuario usuario = actualUsuario.getInfo();

        sb.append("Usuario ").append(i).append("\n");
        sb.append("Nombre: ").append(usuario.getNombre()).append("\n");
        sb.append("Tipo: ").append(usuario.getTipo()).append("\n");
        sb.append("Documentos:\n");

        ListaDocs<Documento> listaDocs = usuario.getDocumentosCreados();

        if (listaDocs == null || listaDocs.esVacia()) {
            sb.append("  - No tiene documentos creados.\n");
        } else {
            NodoLista<Documento> actualDoc = listaDocs.getFirst();

            while (actualDoc != null) {
                Documento doc = actualDoc.getInfo();

                sb.append("  - ID: ").append(doc.getId())
                  .append(" | Tipo: ").append(doc.getTipo())
                  .append(" | Estado: ")
                  .append(doc.isEnCola() ? "EN COLA" : "NO ENVIADO")
                  .append("\n");

                actualDoc = actualDoc.getNext();
            }
        }
        sb.append("\n----------------------------------------\n");
        actualUsuario = actualUsuario.getNext();
        i++;
    }
    MostrarUsuarios.setText(sb.toString());
}
    
    /**
    * Actualiza los combos relacionados con la gestión de documentos de los
    * usuarios, según el usuario actualmente seleccionado y el estado del sistema.
    */
    private void actualizarCombosUsuariosDocumentos() {
        UsuarioCrearDoc.removeAllItems();
        UsuarioGestionDoc.removeAllItems();

        ListaDocs<Usuario> lista = impresora.getUsuariosRegistrados();

        if (lista == null || lista.esVacia()) {
            UsuarioCrearDoc.setEnabled(false);
            UsuarioGestionDoc.setEnabled(false);
            DocumentoGestion.removeAllItems();
            CrearDocumento.setEnabled(false);
            EliminarDocNoEnviado.setEnabled(false);
            EnviarImprimir.setEnabled(false);
            return;
        }

        NodoLista<Usuario> actual = lista.getFirst();
        while (actual != null) {
            String nombre = actual.getInfo().getNombre();
            UsuarioCrearDoc.addItem(nombre);
            UsuarioGestionDoc.addItem(nombre);
            actual = actual.getNext();
        }

        UsuarioCrearDoc.setEnabled(true);
        UsuarioGestionDoc.setEnabled(true);
        CrearDocumento.setEnabled(true);
        actualizarComboDocumentosGestion();
    }
    
    /**
    * Actualiza el combo de documentos disponibles para operaciones de gestión,
    * tomando en cuenta el usuario seleccionado y los documentos asociados a este.
    */
    private void actualizarComboDocumentosGestion() {
        DocumentoGestion.removeAllItems();

        if (UsuarioGestionDoc.getSelectedItem() == null) {
            DocumentoGestion.setEnabled(false);
            EliminarDocNoEnviado.setEnabled(false);
            EnviarImprimir.setEnabled(false);
            return;
        }

        String nombreUsuario = UsuarioGestionDoc.getSelectedItem().toString();
        Usuario usuario = impresora.buscarUsuario(nombreUsuario);

        if (usuario == null || usuario.getDocumentosCreados() == null || usuario.getDocumentosCreados().esVacia()) {
            DocumentoGestion.setEnabled(false);
            EliminarDocNoEnviado.setEnabled(false);
            EnviarImprimir.setEnabled(false);
            return;
        }

        ListaDocs<Documento> listaDocs = usuario.getDocumentosCreados();
        NodoLista<Documento> actual = listaDocs.getFirst();

        while (actual != null) {
            Documento doc = actual.getInfo();

            // Solo muestra documentos no enviados todavía.
            if (!doc.isEnCola()) {
                DocumentoGestion.addItem(doc.getId());
            }

            actual = actual.getNext();
        }
        boolean hayDocs = DocumentoGestion.getItemCount() > 0;
        DocumentoGestion.setEnabled(hayDocs);
        EliminarDocNoEnviado.setEnabled(hayDocs);
        EnviarImprimir.setEnabled(hayDocs);
    }
    
